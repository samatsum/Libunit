#include "../includes/libunit.h"

void	free_test_list(t_unit_test **testlist)
{
	t_unit_test	*temp;
	t_unit_test	*next;

	temp = *testlist;
	while (temp)
	{
		next = temp->next;
		free(temp);
		temp = next;
	}
	*testlist = NULL;
}
